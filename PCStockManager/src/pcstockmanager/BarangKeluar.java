    private void tblbarangkeluarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblbarangkeluarMouseClicked
        // TODO add your handling code here:
        int bar = tblbarangkeluar.getSelectedRow();
        String id_inv = tabmode.getValueAt(bar, 0).toString();
        String id_inv_msk = tabmode.getValueAt(bar, 1).toString();
        String jml = tabmode.getValueAt(bar, 2).toString();
        String tgl = tabmode.getValueAt(bar, 3).toString();
        String ket = tabmode.getValueAt(bar, 4).toString();
        try{
            String sql = "SELECT a.*, b.* FROM trx_barang_masuk a, mst_barang b WHERE a.kd_barang = b.kd_barang AND a.kd_barang_masuk = '" + id_inv_msk + "'";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            if (hasil.next()) {
                txtidbarang.setText(hasil.getString("kd_barang_masuk"));
                txtkode.setText(hasil.getString("kd_barang"));
                txtnama.setText(hasil.getString("nama_barang"));
                txtkategori.setText(hasil.getString("kategori_barang"));
                txtmerek.setText(hasil.getString("merek"));
                txtidkeluar.setText(id_inv);
                txtidbarang.setText(id_inv_msk);
                txtjumlah.setText(jml);
                txtketerangan.setText(ket);
            }            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil " + e);
        }
    }//GEN-LAST:event_tblbarangkeluarMouseClicked

