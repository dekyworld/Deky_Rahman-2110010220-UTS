    protected void autonumber() {
        try {
            String sql = "SELECT kd_barang_keluar FROM trx_barang_keluar ORDER BY kd_barang_keluar DESC LIMIT 1";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            txtidkeluar.setText("OUT0001");
            while (hasil.next()) {
                System.out.println(hasil.getString("kd_barang_keluar").substring(3));
                String id_inventori = hasil.getString("kd_barang_keluar").substring(3);
                int AN = Integer.parseInt(id_inventori) + 1;
                String Nol = "";

                if (AN < 10) {
                    Nol = "000";
                } else if (AN < 100) {
                    Nol = "00";
                } else if (AN < 1000) {
                    Nol = "0";
                } else if (AN < 10000) {
                    Nol = "";
                }

                txtidkeluar.setText("OUT" + Nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto number gagal " + e);
        }
    }
    
