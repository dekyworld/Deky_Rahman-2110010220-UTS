    protected void datatable(){
        Object[] Baris = {"ID Barang Masuk", "Kode Barang", "Jumlah", "Nilai","Tanggal Masuk"};
        tabmode = new DefaultTableModel(null, Baris);
        String cariitem = txtcari.getText();            
        try{
            String sql = "SELECT * FROM trx_barang_masuk WHERE kd_barang_masuk like '%"+cariitem+"%' or kd_barang like '%"+cariitem+"%' order by kd_barang_masuk asc";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                tabmode.addRow(new Object[]{
                    hasil.getString(1),
                    hasil.getString(2),
                    hasil.getString(3),
                    hasil.getString(4),
                    hasil.getString(5)
                });
            }
            tablebarangmasuk.setModel(tabmode);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal di panggil"+e);
        }
    }

