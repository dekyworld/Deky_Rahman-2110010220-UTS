    private void tablebarangmasukMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablebarangmasukMouseClicked
        // TODO add your handling code here:
        int bar = tablebarangmasuk.getSelectedRow();
        String id_barang = tabmode.getValueAt(bar, 0).toString();
        String kd_brg = tabmode.getValueAt(bar, 1).toString();
        String jml = tabmode.getValueAt(bar, 2).toString();
        String nil = tabmode.getValueAt(bar, 3).toString();
        try{
            String sql = "SELECT * FROM mst_barang WHERE kd_barang='"+kd_brg+"'";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            if (hasil.next()) {
                txtnama.setText(hasil.getString("nama_barang"));
                txtkategori.setText(hasil.getString("kategori_barang"));
                txtmerek.setText(hasil.getString("merek"));
            }    

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dipanggil " + e);
        }
        txtkode.setText(kd_brg);
        txtjumlah.setText(jml);
        txtnilai.setText(nil);
    }//GEN-LAST:event_tablebarangmasukMouseClicked

