    protected void autonumber() {
        try {
            String sql = "SELECT kd_barang_masuk FROM trx_barang_masuk ORDER BY kd_barang_masuk DESC LIMIT 1";
            Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            txtidbarang.setText("IN0001");
            while (hasil.next()) {
                System.out.println(hasil.getString("kd_barang_masuk").substring(2));
                String id_inventori = hasil.getString("kd_barang_masuk").substring(2);
                int AN = Integer.parseInt(id_inventori) + 1;
                String Nol = "";

                if (AN < 10) {
                    Nol = "000";
                } else if (AN < 100) {
                    Nol = "00";
                } else if (AN < 1000) {
                    Nol = "0";
                } else if (AN < 10000) {
                    Nol = "";
                }

                txtidbarang.setText("IN" + Nol + AN);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Auto number gagal " + e);
        }
    }
    
